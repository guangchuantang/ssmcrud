package com.cn.hnust.pojo;

public class Teacher {
	
	private Integer id;
	private String picture;
	private String name;
	private String sex;
	private String school;
	private String course;
	private String goodness;
	private String phone;
	private String price;
	private String elegantsShow;
	private String comment;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getGoodness() {
		return goodness;
	}
	public void setGoodness(String goodness) {
		this.goodness = goodness;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getElegantsShow() {
		return elegantsShow;
	}
	public void setElegantsShow(String elegantsShow) {
		this.elegantsShow = elegantsShow;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
}
